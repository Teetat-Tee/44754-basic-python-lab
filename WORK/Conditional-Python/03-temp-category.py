c = int(1) # รับค่าจากผู้ใช้
if(c <= 0): # ตรวจสอบว่าค่าที่รับเข้ามาน้อยกว่าหรือเท่ากับ 0 หรือไม่
    print("Freezing") # ถ้าค่าน้อยกว่าหรือเท่ากับ 0 ให้พิมพ์ "Freezing"
elif(c >= 1 and c < 16): # ถ้าค่ามากกว่าหรือเท่ากับ 1 และน้อยกว่า 16
    print("Cold") # ให้พิมพ์ "Cold"
elif(c >= 16 and c < 31): # ถ้าค่ามากกว่าหรือเท่ากับ 16 และน้อยกว่า 31
    print("Warm") # ให้พิมพ์ "Warm"
else:
    print("Hot") # ถ้าค่ามากกว่าหรือเท่ากับ 31 ให้พิมพ์ "Hot"