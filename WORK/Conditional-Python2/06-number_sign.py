number = int(input("Enter your number: ")) #รับค่า
if(number > 0): # ตรวจสอบว่ามากกว่า 0 หรือไม่
    print("Positive") # แสดงผลลัพธ์ถ้าบวก
elif(number == 0): # ตรวจสอบว่าเท่ากับ 0 หรือไม่
    print("Zero") # แสดงผลลัพธ์ถ้าเป็น 0
elif(number < 0): # ตรวจสอบว่าน้อยกว่า 0 หรือไม่
    print("Negative")  # แสดงผลลัพธ์ถ้าเป็นลบ