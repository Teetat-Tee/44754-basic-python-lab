    if (year % 100 != 0) or (year % 400 == 0): # ถ้าปีหารด้วย 100 ไม่ลงตัว หรือหารด้วย 400 ลงตัว
        print("Leap Year") # ถ้าเงื่อนไขเป็นจริง แสดงว่าเป็นปีอธิกสุรทิน
